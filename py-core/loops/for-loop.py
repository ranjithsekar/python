# each char
print('===Iterate a string')
name = 'Ranjith'
for e in name:
    print(e)

# array
print('===Iterator over Array')
fruits = ['Apple', 'Orange', 'Guava', 'Banana']
for fruit in fruits:
    print(fruit)

# range
print('===RANGE(?,?)')
for val in range(2, 6):
    print(val)

print('===RANGE(?,?,?)')
for val in range(2, 9, 4):
    print(val)