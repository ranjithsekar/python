# Declaration
my_num = 5
my_string = 'Ranjith'

print(my_num)
print(my_string)

# Casting
my_num1 = '4'
my_num1_int = int(my_num1)
print(my_num1_int)

my_num2 = 3
my_num2_str = str(my_num2)
print(my_num2_str)

my_num3 = '6'
my_num3_float = float(my_num3)
print(my_num3_float)

# Get the type
print(type(my_num3))
print(type(my_num3_float))

# Multiple values
one, two, three = "Lion", "Tiger", "Elephant"
print(one + ' ' + two + ' ' + three)
