# String
myName = "Ranjith"
print('myName: ' + myName)

quote1 = "Ranjith!, it's me"
print("double quote: " + quote1)

quote2 = 'Hello! "Good Morning!!"'
print('Single quote: ' + quote2)

# multi-line string
address = """
2nd Street,
Gandhi Nagar,
Chennai,
600078
"""
print('Triple double quote: ' + address)

message = '''
This is my first message.
from the office,
at chennai.
'''
print('Triple single quote: ' + message)

# string concat
firstname = "Ranjith"
lastname = "Sekar"
name = firstname + " " + lastname
print(name)